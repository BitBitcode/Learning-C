// 36、一元钱兑换方案

// 一元钱可以兑换成一角、两角、五角，求有多少种兑换方式？

#include<stdio.h>

void main()
{
	int i;						// 一角钱的个数
	int n;						// 两角钱的个数
	int k;						// 五角钱的个数

	for (i = 0; i <= 10; i++)
	{
		for (n = 0; n <= 5; n++)
		{
			for (k = 0; k < 2; k++)
			{
				if (i + 2 * n + 5 * k == 10)
				{
					printf("一角钱 %d，两角钱 %d，五角钱 %d；\n", i, n, k);
				}
			}
		}
	}
}
